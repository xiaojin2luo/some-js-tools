            for (let x = 0, width = xyArray[0].length; x < width; x++) {
                sumY += xyArray[y][x];
            }
            // 将需要切丢的列的索引添加进数组
            if (sumY === 0) spliceIndex.push(y);
            // console.log(sumY);
